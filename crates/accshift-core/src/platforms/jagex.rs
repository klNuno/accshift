/// Jagex writes no readable account id anywhere, so ids are generated by us
/// when an account is captured (TcNo does the same with an id file).
fn generate_account_id() -> String {
    Uuid::new_v4().simple().to_string()
}

fn is_valid_jagex_account_id(s: &str) -> bool {
    // Synthetic ids are 32-char hex UUIDs. The id is joined into filesystem
    // paths (auth_cache_dir), so restrict the charset and length to keep path
    // traversal out.
    !s.is_empty() && s.len() <= 64 && s.chars().all(|c| c.is_ascii_alphanumeric())
}

