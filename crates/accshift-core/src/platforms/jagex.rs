fn validate_account_id(id: &str) -> Result<String, String> {
    let trimmed = id.trim().to_string();
    if trimmed.is_empty() {
        return Err("Empty Jagex account ID".into());
    }
    // Strict format check: the id is joined into filesystem paths
    // (auth_cache_dir), so anything outside the synthetic id charset must be
    // rejected.
    if !is_valid_jagex_account_id(&trimmed) {
        return Err(format!("Invalid Jagex account ID: {trimmed}"));
    }
    Ok(trimmed)
}

/// Accounts only exist in the config: Jagex leaves no on-disk id to discover,
/// so every account was created by our own setup flow.
fn read_accounts(app_handle: &dyn AppContext) -> Result<Vec<JagexAccount>, String> {
    let cfg = config::load_config(app_handle);

    let mut seen = HashSet::new();
    let mut accounts = Vec::new();
    for account in &cfg.jagex.accounts {
        let key = account.account_id.trim().to_string();
        if key.is_empty() || !seen.insert(key.clone()) {
            continue;
        }
        accounts.push(JagexAccount {
            account_id: key.clone(),
            label: account.label.trim().to_string(),
            last_used_at: account.last_used_at,
            snapshot_saved: has_auth_snapshot(app_handle, &key),
        });
    }

    Ok(accounts)
}

/// The current account is whichever id was last switched to or captured by
/// setup. There is no live source of truth to cross-check against.
fn read_current_account(app_handle: &dyn AppContext) -> Option<String> {
    let cfg = config::load_config(app_handle);
    let current = cfg.jagex.current_account.trim().to_string();
    if current.is_empty() || !is_valid_jagex_account_id(&current) {
        None
    } else {
        Some(current)
    }
}

fn remember_account_usage(app_handle: &dyn AppContext, account_id: &str) -> Result<(), String> {
    let account_id = validate_account_id(account_id)?;
    let key = account_id.clone();
    let now = super::now_unix_ms();

    config::update_config(app_handle, |cfg| {
        if let Some(existing) = cfg
            .jagex
            .accounts
            .iter_mut()
            .find(|a| a.account_id.trim() == key)
        {
            existing.last_used_at = Some(now);
        } else {
            cfg.jagex.accounts.push(JagexAccountConfig {
                account_id: account_id.clone(),
                label: String::new(),
                last_used_at: Some(now),
            });
        }
    })
}

/// Record usage of the current account and refresh its snapshot before its
/// live session is overwritten.
///
/// A session signed in outside accshift has no known id (Jagex writes none),
/// so with no current account in the config there is nothing we can attribute
/// the live session to and capture is skipped.
///
/// Returns Err when a current account IS recorded but its snapshot could not
/// be saved, so the caller can abort before killing the launcher or replacing
/// the live session.
fn capture_current_account(app_handle: &dyn AppContext) -> Result<(), String> {
    let Some(current_id) = read_current_account(app_handle) else {
        return Ok(());
    };
    let _ = remember_account_usage(app_handle, &current_id);
    save_auth_snapshot(app_handle, &current_id)
}

fn forget_account_metadata(app_handle: &dyn AppContext, account_id: &str) -> Result<(), String> {
    let key = account_id.trim().to_string();
    config::update_config(app_handle, |cfg| {
        cfg.jagex.accounts.retain(|a| a.account_id.trim() != key);
        if cfg.jagex.current_account.trim() == key {
            cfg.jagex.current_account.clear();
        }
    })?;

    // Remove cached auth snapshot. Only touch the filesystem for ids in the
    // canonical synthetic format: the id is joined into the snapshot path.
    if is_valid_jagex_account_id(&key) {
        if let Ok(cache_dir) = auth_cache_dir(app_handle, &key) {
            // Free the OS-keyring entries every encrypted file points at before
            // deleting them (no-op under Windows DPAPI).
            delete_encrypted_file_secret(&cache_dir.join(SETTINGS_JSON));
            delete_encrypted_file_secret(&cache_dir.join(LAUNCHER_JSON));
            delete_encrypted_file_secret(&cache_dir.join(SNAP_CRED_BLOB));
            free_dir_secrets(&cache_dir.join(SNAP_AUTH));
            let _ = fs::remove_dir_all(&cache_dir);
        }
    }

    Ok(())
}

