const JAGEX_PROCESS_NAMES: &[&str] = &["JagexLauncher.exe"];
const JAGEX_EXECUTABLE_NAME: &str = "JagexLauncher.exe";
const JAGEX_SETUP_TTL_MS: u64 = 5 * 60 * 1000;
const POST_KILL_SETTLE_MS: u64 = 500;
/// Longest we wait for the launcher to flush its auth files to disk and exit
/// after a quit request before validating the snapshot source.
const SETUP_QUIT_TIMEOUT_MS: u32 = 8000;

/// Per-account files that live directly under %LOCALAPPDATA%\jagex launcher.
const SETTINGS_JSON: &str = "settings.json";
const LAUNCHER_JSON: &str = "launcher-win.production.json";

/// The launcher stores its Credential Manager entry as a DPAPI blob at this
/// fixed file name under %LOCALAPPDATA%\Microsoft\Credentials. The blob is
/// already user-bound encrypted; we snapshot and restore it as an opaque file
/// and never decrypt it ourselves.
const CRED_BLOB_LIVE_NAME: &str = "C5F6B4FF052E05B4F26961945E953169";

/// Snapshot names inside an account's cache directory.
const SNAP_AUTH: &str = "auth";
const SNAP_CRED_BLOB: &str = "credman_blob.bin";

