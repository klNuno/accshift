pub fn get_accounts(app_handle: &dyn AppContext) -> Result<Vec<JagexAccount>, String> {
    read_accounts(app_handle)
}

pub fn get_startup_snapshot(app_handle: &dyn AppContext) -> Result<JagexStartupSnapshot, String> {
    let accounts = read_accounts(app_handle)?;
    let current = read_current_account(app_handle).unwrap_or_default();
    Ok(JagexStartupSnapshot {
        accounts,
        current_account: current,
    })
}

pub fn get_current_account(app_handle: &dyn AppContext) -> Result<String, String> {
    Ok(read_current_account(app_handle).unwrap_or_default())
}

pub fn switch_account(app_handle: &dyn AppContext, account_id: &str) -> Result<(), String> {
    let account_id = validate_account_id(account_id)?;
    log_platform_info(
        app_handle,
        "jagex.switch_account",
        "Jagex switch requested",
        format!("target={}", super::redact_id(&account_id)),
    );

    // Always record + snapshot the current account before switching away.
    // Abort here if the snapshot cannot be saved: proceeding would kill the
    // launcher and overwrite this account's live session with the target's,
    // stranding it signed out with no backup to restore later.
    capture_current_account(app_handle)?;

    // Clear the current marker before touching live files: if the restore
    // fails midway the live state is a mix of two accounts and must not be
    // captured into either snapshot on a later switch.
    config::update_config(app_handle, |cfg| cfg.jagex.current_account.clear())?;

    // Kill the launcher and wait for it to actually exit before touching files.
    quit_jagex_and_wait();

    // Restore target account's session
    restore_auth_snapshot(app_handle, &account_id)?;

    // Record usage and mark the target as the live session.
    remember_account_usage(app_handle, &account_id)?;
    config::update_config(app_handle, |cfg| {
        cfg.jagex.current_account = account_id.clone();
    })?;

    // Relaunch
    let result = launch_jagex(app_handle);

    match &result {
        Ok(()) => log_platform_info(
            app_handle,
            "jagex.switch_account",
            "Jagex switch completed",
            format!("target={}", super::redact_id(&account_id)),
        ),
        Err(error) => log_platform_error(
            app_handle,
            "jagex.switch_account",
            "Jagex switch failed",
            format!("target={}; error={error}", super::redact_id(&account_id)),
        ),
    }

    result
}

pub fn begin_account_setup(app_handle: &dyn AppContext) -> Result<SetupStatus, String> {
    log_platform_info(
        app_handle,
        "jagex.begin_account_setup",
        "Jagex account setup requested",
        "",
    );

    // Record + snapshot the current account before clearing its session. Abort
    // if the snapshot cannot be saved: proceeding would kill the launcher and
    // delete the live session with no backup to restore later.
    capture_current_account(app_handle)?;

    let setup_id = format!("jagex-setup-{}", Uuid::new_v4());
    let created_at = super::now_unix_ms();

    let mut jobs = setup_jobs()
        .lock()
        .map_err(|_| "Jagex setup storage is unavailable".to_string())?;
    jobs.retain(|_, j| !super::setup_expired(j.last_touched_at, JAGEX_SETUP_TTL_MS));
    jobs.insert(
        setup_id.clone(),
        JagexSetupJob {
            last_touched_at: created_at,
        },
    );
    drop(jobs);

    // Kill the launcher, clear the live session to force the login screen.
    quit_jagex_and_wait();
    delete_auth_files()?;
    // Nobody is signed in until the setup completes.
    config::update_config(app_handle, |cfg| cfg.jagex.current_account.clear())?;

    // Relaunch
    launch_jagex(app_handle).inspect_err(|e| {
        log_platform_error(
            app_handle,
            "jagex.begin_account_setup",
            "Jagex setup launch failed",
            e,
        );
    })?;

    Ok(super::make_setup_status(
        &setup_id,
        "waiting_for_client",
        "",
        "",
        "",
    ))
}

pub fn get_account_setup_status(
    app_handle: &dyn AppContext,
    setup_id: &str,
) -> Result<SetupStatus, String> {
    {
        let mut jobs = setup_jobs()
            .lock()
            .map_err(|_| "Jagex setup storage is unavailable".to_string())?;
        jobs.retain(|_, j| !super::setup_expired(j.last_touched_at, JAGEX_SETUP_TTL_MS));
        let Some(job) = jobs.get_mut(setup_id) else {
            return Err("Jagex setup session not found".into());
        };
        job.last_touched_at = super::now_unix_ms();
    }

    // Setup deleted the auth dir and credential blob, so fresh session
    // material means the user signed in.
    if login_artifacts_present() {
        // Quit the launcher so any in-memory session state flushes to disk,
        // then re-check before capturing.
        quit_jagex_and_wait();

        if !login_artifacts_present() {
            return Ok(super::make_setup_status(
                setup_id,
                "waiting_for_login",
                "",
                "",
                "",
            ));
        }

        let key = generate_account_id();
        save_auth_snapshot(app_handle, &key)?;
        remember_account_usage(app_handle, &key)?;
        config::update_config(app_handle, |cfg| {
            cfg.jagex.current_account = key.clone();
        })?;

        if let Ok(mut jobs) = setup_jobs().lock() {
            jobs.remove(setup_id);
        }

        // No display name: Jagex exposes none, the user labels the account.
        return Ok(super::make_setup_status(setup_id, "ready", key, "", ""));
    }

    if is_jagex_running() {
        return Ok(super::make_setup_status(
            setup_id,
            "waiting_for_login",
            "",
            "",
            "",
        ));
    }

    Ok(super::make_setup_status(
        setup_id,
        "waiting_for_client",
        "",
        "",
        "",
    ))
}

pub fn cancel_account_setup(setup_id: &str) -> Result<(), String> {
    let mut jobs = setup_jobs()
        .lock()
        .map_err(|_| "Jagex setup storage is unavailable".to_string())?;
    jobs.retain(|_, j| !super::setup_expired(j.last_touched_at, JAGEX_SETUP_TTL_MS));
    jobs.remove(setup_id);
    Ok(())
}

pub fn forget_account(app_handle: &dyn AppContext, account_id: &str) -> Result<(), String> {
    let account_id = validate_account_id(account_id)?;
    forget_account_metadata(app_handle, &account_id)
}

pub fn set_account_label(
    app_handle: &dyn AppContext,
    account_id: &str,
    label: &str,
) -> Result<(), String> {
    let account_id = validate_account_id(account_id)?;
    let key = account_id.clone();
    let label = label.trim().to_string();

    config::update_config(app_handle, |cfg| {
        if let Some(existing) = cfg
            .jagex
            .accounts
            .iter_mut()
            .find(|a| a.account_id.trim() == key)
        {
            existing.label = label.clone();
        } else {
            cfg.jagex.accounts.push(JagexAccountConfig {
                account_id: account_id.clone(),
                label,
                last_used_at: None,
            });
        }
    })
}

pub fn get_jagex_path(app_handle: &dyn AppContext) -> Result<String, String> {
    let cfg = config::load_config(app_handle);
    if !cfg.jagex.path_override.trim().is_empty() {
        return Ok(cfg.jagex.path_override);
    }
    resolve_executable(app_handle).map(|p| p.to_string_lossy().to_string())
}

pub fn set_jagex_path(app_handle: &dyn AppContext, path: &str) -> Result<(), String> {
    let path = path.trim().to_string();
    config::update_config(app_handle, |cfg| {
        cfg.jagex.path_override = path;
    })
}

pub fn select_jagex_path() -> Result<String, String> {
    crate::os::select_file(
        "Select Jagex Launcher executable",
        "Executable files (*.exe)|*.exe|All files (*.*)|*.*",
    )
    .map_err(|e| e.to_string())
}

