fn local_app_data_dir() -> Result<PathBuf, String> {
    let local_app_data =
        env::var("LOCALAPPDATA").map_err(|_| "LOCALAPPDATA is not available".to_string())?;
    Ok(PathBuf::from(local_app_data))
}

fn jagex_data_dir() -> Result<PathBuf, String> {
    Ok(local_app_data_dir()?.join("jagex launcher"))
}

fn jagex_auth_dir() -> Result<PathBuf, String> {
    Ok(jagex_data_dir()?.join("auth"))
}

fn credential_blob_path() -> Result<PathBuf, String> {
    Ok(local_app_data_dir()?
        .join("Microsoft")
        .join("Credentials")
        .join(CRED_BLOB_LIVE_NAME))
}

fn jagex_default_executable() -> Option<PathBuf> {
    for var in ["ProgramFiles(x86)", "ProgramFiles"] {
        if let Ok(pf) = env::var(var) {
            let path = PathBuf::from(&pf)
                .join("Jagex Launcher")
                .join(JAGEX_EXECUTABLE_NAME);
            if path.is_file() {
                return Some(path);
            }
        }
    }
    None
}

fn resolve_executable(app_handle: &dyn AppContext) -> Result<PathBuf, String> {
    let cfg = config::load_config(app_handle);
    let override_path = cfg.jagex.path_override.trim().to_string();
    if !override_path.is_empty() {
        let p = PathBuf::from(&override_path);
        if p.is_file() {
            return Ok(p);
        }
        let candidate = p.join(JAGEX_EXECUTABLE_NAME);
        if candidate.is_file() {
            return Ok(candidate);
        }
    }

    if let Some(exe) = jagex_default_executable() {
        return Ok(exe);
    }

    Err("Could not locate Jagex Launcher executable".into())
}

